    private void submitBtnMouseClicked(java.awt.event.MouseEvent evt) {
        resetForm();
        boolean errors = false;
        if (givenNameField.getText().isEmpty()) {
            errors = true;
            givenNameField.setBorder(new LineBorder(Color.red, 1));
        }
        if (surnameField.getText().isEmpty()) {
            errors = true;
            surnameField.setBorder(new LineBorder(Color.red, 1));
        }
        if (address1Field.getText().isEmpty()) {
            errors = true;
            address1Field.setBorder(new LineBorder(Color.red, 1));
        }
        if (townField.getText().isEmpty()) {
            errors = true;
            townField.setBorder(new LineBorder(Color.red, 1));
        }
        if (!postCodePattern.matcher(postalCodeField.getText()).matches()) {
            errors = true;
            postalCodeField.setBorder(new LineBorder(Color.red, 1));
        }
        if (!emailPattern.matcher(emailAddressField.getText()).matches()) {
            errors = true;
            emailAddressField.setBorder(new LineBorder(Color.red, 1));
        }
        if (!phoneNumberField.getText().isEmpty()) {
            if (!phoneNumberPattern.matcher(phoneNumberField.getText()).matches()) {
                errors = true;
                phoneNumberField.setBorder(new LineBorder(Color.red, 1));
            }
        }
        if (errors) return;
        Person p = getEntityFromForm();
        personService.createPerson(p);
        resetForm();
        resetFormData();
        JOptionPane.showMessageDialog(f, "User " + p.getGivenName() + " " + p.getSurname() + " was added succecssfully!");
    }

