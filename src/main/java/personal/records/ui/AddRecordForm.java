    public Person getEntityFromForm() {
        Integer postCode = null;
        Long phoneNumber = null;
        if (!postalCodeField.getText().isEmpty()) {
            postCode = Integer.parseInt(postalCodeField.getText());
        }
        if (!phoneNumberField.getText().isEmpty()) {
            phoneNumber = Long.parseLong(phoneNumberField.getText());
        }
        return new Person(
                givenNameField.getText(),
                surnameField.getText(),
                address1Field.getText(),
                address2Field.getText(),
                townField.getText(),
                countyField.getText(),
                (Country) countryField.getSelectedItem(),
                emailAddressField.getText(),
                postCode,
                phoneNumber
        );
    }

